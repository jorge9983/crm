     */
    public function store(Request $request)
    {
        $empresa = new Empresa();
        $empresa->nombre = $request->nombre;
        $empresa->correo = $request->correo;
        $empresa->sitioweb = $request->sitioweb;
        $empresa->save();

        return back()->with('mensaje', 'Empresa Agregada!');
    }

